int	ft_sqrt(int nb)
{
	int	c;
	int sg;

	c = 0;
	while (c <= nb / c)
	{
		sg = c * c;
		if (sg == nb)
		{
			return (c);
		}
		else
		{
			c++;
		}
	}
	return (0);
}
